package com.example.gaid.model.get_info;

public class GetInfoResponseDTO {

    private int room_idx;
    private String room_no;
    private String room_floor;
    private String room_department;
    private String room_professor;
    private String room_tel;
    private String room_name;
    private String room_business;

    public int getRoom_idx() {
        return room_idx;
    }

    public void setRoom_idx(int room_idx) {
        this.room_idx = room_idx;
    }

    public String getRoom_no() {
        return room_no;
    }

    public void setRoom_no(String room_no) {
        this.room_no = room_no;
    }

    public String getRoom_floor() {
        return room_floor;
    }

    public void setRoom_floor(String room_floor) {
        this.room_floor = room_floor;
    }

    public String getRoom_department() {
        return room_department;
    }

    public void setRoom_department(String room_department) {
        this.room_department = room_department;
    }

    public String getRoom_professor() {
        return room_professor;
    }

    public void setRoom_professor(String room_professor) {
        this.room_professor = room_professor;
    }

    public String getRoom_tel() {
        return room_tel;
    }

    public void setRoom_tel(String room_tel) {
        this.room_tel = room_tel;
    }

    public String getRoom_name() {
        return room_name;
    }

    public void setRoom_name(String room_name) {
        this.room_name = room_name;
    }

    public String getRoom_business() {
        return room_business;
    }

    public void setRoom_business(String room_business) {
        this.room_business = room_business;
    }
}
